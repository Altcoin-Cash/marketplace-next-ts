import type { NextPage } from "next";
import styles from "../../styles/Home.module.css";
import Link from "next/link";
import {
  MediaRenderer,
  useActiveListings,
  useMarketplace,
} from "@thirdweb-dev/react";
import { useRouter } from "next/router";

const Home: NextPage = () => {
  const router = useRouter();
  const { nftId } = router.query;

  // Connect your marketplace smart contract here (replace this address)
  const marketplace = useMarketplace(
    "0xD0804F2cDFC75A308d786DcA78f0DC617d991CaE" // Your marketplace contract address here
  );

  const { data: listings, isLoading: loadingListings } =
    useActiveListings(marketplace);

  return (
    <>
      {/* Content */}
      <div className={styles.container}>
        {/* Top Section */}
        <h1 className={styles.h1}>Mining Game NFT Marketplace</h1>
        <p className={styles.explain}>
          Buy{" "}
          <b>
            {" "}
            <a
              href="pages/[nftId]/index.tsx"
              target="_blank"
              rel="noopener noreferrer"
              className={styles.purple}
            >
              Mining Game
            </a>
          </b>{" "}
          NFTs.
        </p>
        <p className={styles.explain}>WATT received here WILL BE BURNED!</p>
        <hr className={styles.divider} />

        <div className="main">
          {
            // If the listings are loading, show a loading message
            loadingListings ? (
              <div>Loading listings...</div>
            ) : (
              // Otherwise, show the listings
              <div className={styles.listingGrid}>
                {listings
//                  ?.filter((listing) => listing.asset.id.toNumber() === Number(nftId))
                   ?.filter((listing) => Number(listing.asset.id) === (nftId))
                  .map((listing) => (
                    <div
                      key={listing.id}
                      className={styles.listingShortView}
                      onClick={() =>
                        router.push(`/${nftId}/listing/${listing.id}`)
                      }
                    >
                      <MediaRenderer
                        src={listing.asset.image}
                        style={{
                          borderRadius: 16,
                          // Fit the image to the container
                          width: "100%",
                          height: "100%",
                        }}
                      />
                      <h2 className={styles.nameContainer}>
                        <Link href={`/${nftId}/listing/${listing.id}`}>
                          <a className={styles.name}>{listing.asset.name}</a>
                        </Link>
                      </h2>

                      <p>
                        <b>
                          {listing.buyoutCurrencyValuePerToken.displayValue}
                        </b>{" "}
                        {listing.buyoutCurrencyValuePerToken.symbol}
                      </p>
                    </div>
                  ))}
              </div>
            )
          }
        </div>
      </div>
    </>
  );
};

export default Home;
